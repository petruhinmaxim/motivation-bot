export type MissedDaysTextKind = 1 | 2 | 3;

function clampMissedDaysKind(daysWithoutWorkout: number): MissedDaysTextKind {
  if (daysWithoutWorkout <= 1) return 1;
  if (daysWithoutWorkout === 2) return 2;
  return 3;
}

